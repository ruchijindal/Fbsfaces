package com.smp.fbs;

/**
 *
 * @author smp7
 */
public class FlatInfo {
    public String floorNo;
    public String flatNo;
    public String flatId;
    public String flatTypeId;
    public String flatSpecification;
    public String status;

    public String getFloorNo() {
        return floorNo;
    }

    public void setFloorNo(String floorNo) {
        this.floorNo = floorNo;
    }
    
    public String getFlatId() {
        return flatId;
    }

    public void setFlatId(String flatId) {
        this.flatId = flatId;
    }

    public String getFlatNo() {
        return flatNo;
    }

    public void setFlatNo(String flatNo) {
        this.flatNo = flatNo;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getFlatSpecification() {
        return flatSpecification;
    }

    public void setFlatSpecification(String flatSpecification) {
        this.flatSpecification = flatSpecification;
    }

    public String getFlatTypeId() {
        return flatTypeId;
    }

    public void setFlatTypeId(String flatTypeId) {
        this.flatTypeId = flatTypeId;
    }
    
    
}
